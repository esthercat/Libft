#include "libft.h"

char		*ft_str_tolower(char *str)
{
	int i;

	i = 0;
	while (str)
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] += 32;
		i++;
	}
	return (str);
}
