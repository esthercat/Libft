#include "libft.h"

int	ft_atoi(const char *str)
{
	int i;
	int nega;
	int result;

	i = 0;
	result = 0;
	nega = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-')
		nega = -1;
	if ((str[i] == '-') || (str[i] == '+'))
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = result * 10 + (str[i] - '0');
		i++;
	}
	return (result * nega);
}
